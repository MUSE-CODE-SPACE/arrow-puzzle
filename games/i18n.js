// Detect language from URL param or localStorage
function getGameLang() {
  const params = new URLSearchParams(window.location.search);
  return params.get('lang') || localStorage.getItem('puzzleArcadeLang') || 'ko';
}

function getTranslations() {
  const lang = getGameLang();
  return GAME_I18N[lang] || GAME_I18N.ko;
}

// Helper: apply translations to elements with data-i18n attribute
function applyGameI18n() {
  const t = getTranslations();
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key]) el.textContent = t[key];
  });
}

// Helper: get back link with lang preserved
function getBackLink() {
  const lang = getGameLang();
  return '../index.html';
}
